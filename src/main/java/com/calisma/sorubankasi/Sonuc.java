package com.calisma.sorubankasi;

public class Sonuc {

	private String sayi, yanit, sonuc, dogruYanit;

	public String getSayi() {
		return sayi;
	}

	public void setSayi(String sayi) {
		this.sayi = sayi;
	}

	public String getYanit() {
		return yanit;
	}

	public void setYanit(String yanit) {
		this.yanit = yanit;
	}

	public String getSonuc() {
		return sonuc;
	}

	public void setSonuc(String sonuc) {
		this.sonuc = sonuc;
	}

	public String getDogruYanit() {
		return dogruYanit;
	}

	public void setDogruYanit(String dogruYanit) {
		this.dogruYanit = dogruYanit;
	}
	
	
	
}
